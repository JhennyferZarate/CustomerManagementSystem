    private void updateList(){
        ClientDAO dao = new ClientDAO();
        list = dao.list();
        
        DefaultListModel data = new DefaultListModel();
        for(int i = 0 ; i < list.size(); i++){
            Client client = list.get(i);
            data.addElement(client.getFullName());
        }
        this.listClient.setModel(data);
    }
    
    private void clearTextBoxes(){
        this.lbId.setText("");
        this.txtDNI.setText("");
        this.txtName.setText("");
        this.txtLastName.setText("");
        this.txtPhone.setText("");
        this.txtEmail.setText("");
    }
    
    private boolean validateMail(){
        Pattern pattern = Pattern
                .compile("^[_A-Za-z0-9-\\+]+(\\.[_A-Za-z0-9-]+)*@"
                        + "[A-Za-z0-9-]+(\\.[A-Za-z0-9]+)*(\\.[A-Za-z]{2,})$");
        
        Matcher mather = pattern.matcher(this.txtEmail.getText());
        
        if(!mather.find()){
            JOptionPane.showMessageDialog(rootPane, "Invalid email. Enter again.");
            this.txtEmail.setText("");
            return false;
        }else{
            return true;
        }
    }
    
    private boolean validateDNI(){
        if(this.txtDNI.getText().length() != 8){
            JOptionPane.showMessageDialog(rootPane, "The DNI must have 8 digits.");
            return false;
        }else{
            return true;
        }
    }
    
