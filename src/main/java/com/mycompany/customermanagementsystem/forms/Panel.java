    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
    
        //Empty fields?
        if(this.txtDNI.getText().length() == 0 ||
           this.txtName.getText().length() == 0 ||
           this.txtLastName.getText().length() == 0 ||
           this.txtPhone.getText().length() == 0 ||
           this.txtEmail.getText().length() == 0){
            
            JOptionPane.showMessageDialog(rootPane, "There are empty fields, you must fill in to save.");
        }else{
            if(validateMail() && validateDNI()){
                Client client = new Client();
                client.setDNI(Integer.parseInt(this.txtDNI.getText()));
                client.setName(this.txtName.getText());
                client.setLastName(this.txtLastName.getText());
                client.setPhone(this.txtPhone.getText());
                client.setEmail(this.txtEmail.getText());
                
                if(!StringUtils.isEmptyOrWhitespaceOnly(lbId.getText())){
                    client.setId(lbId.getText());
                }
                
                ClientDAO dao = new ClientDAO();
                dao.save(client);
            
                updateList();
                JOptionPane.showMessageDialog(rootPane, "The client was saved successfully.");
                clearTextBoxes();  
            }   
        }     
    }//GEN-LAST:event_btnSaveActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        int index = this.listClient.getSelectedIndex();
        Client client = list.get(index);
        ClientDAO dao = new ClientDAO();
        dao.deleteClient(client.getId());
        JOptionPane.showMessageDialog(rootPane, "Client deleted." );
        updateList();
    }//GEN-LAST:event_btnDeleteActionPerformed

